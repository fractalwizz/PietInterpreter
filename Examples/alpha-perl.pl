sub doadd {
    $one = pop(@stack);
    $two = pop(@stack);
    unless (not defined $one || not defined $two) { push(@stack, $one + $two); }
};
sub dogreat {
    $one = pop(@stack);
    $two = pop(@stack);
    $res = (defined $one && defined $two && $two > $one) ? 1 : 0;
    push(@stack, $res);
}
sub dodup {
    $one = pop(@stack);
    if (defined $one) {
        push(@stack, $one);
        push(@stack, $one);
    }
}
sub dopush {
    $val = shift;
    push(@stack, $val);
}
sub dosub {
    $one = pop(@stack);
    $two = pop(@stack);
    unless (not defined $one || not defined $two) { push(@stack, $two - $one); }
};
