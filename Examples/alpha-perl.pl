sub domul {
    $one = pop(@stack);
    $two = pop(@stack);
    unless (not defined $one || not defined $two) { push(@stack, $one * $two); }
};
sub donot {
    $one = pop(@stack);
    $res = (defined $one && $one == 0) ? 1 : 0;
    push(@stack, $res);
}
sub dooutC {
    $val = pop(@stack);
    unless (not defined $val) { print chr $val; }
}
